public class Product {
    int model;
    int price;

    public Product(int model, int price) {
        this.model = model;
        this.price = price;
    }
    
    public int getModel() {
        return model;
    }
    public void setModel(int model) {
        this.model = model;
    }
    public int getPrice() {
        return price;
    }
    public void setPrice(int price) {
        this.price = price;
    }

    
}
